package com.example.osads.mlcplus;




public class Rival {

    private String rivalName;
    private Gender rivalGender;
    private int rivalLevel;
    private int rivalBonus;
    private int rivalStrength;

    public String getRivalName() {
        return rivalName;
    }

    public void setRivalName(String rivalName) {
        this.rivalName = rivalName;
    }

    public Gender getRivalGender() {
        return rivalGender;
    }

    public void setRivalGender(Gender rivalGender) {
        this.rivalGender = rivalGender;
    }

    public int getRivalLevel() {
        return rivalLevel;
    }

    public void setRivalLevel(int rivalLevel) {
        this.rivalLevel = rivalLevel;
    }

    public int getRivalBonus() {
        return rivalBonus;
    }

    public void setRivalBonus(int rivalBonus) {
        this.rivalBonus = rivalBonus;
    }

    public int getRivalStrength() {
        return rivalStrength;
    }

    public void setRivalStrength(int rivalStrength) {
        this.rivalStrength = rivalStrength;
    }
}
